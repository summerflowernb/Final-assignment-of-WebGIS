//添加一个窗体函数，用来隐藏箭头
$(document).ready(function() {
	/* 窗体滚动事件 */
	$(window).scroll(function() {
		//判断滚动条的垂直位置是否大于0，说白了就是：判断滚动条是否在顶部
		if ($(window).scrollTop() > 0) {
			$("#button1").stop(true, false).animate({
				bottom: "-300px"
			}, 1000); //动画隐藏ID="doTop"的这个层
			// $("#button1").hide(),1000); //动画隐藏ID="doTop"的这个层
		} else {
			$("#button1").stop(true, false).animate({
				bottom: "30px"
			}, 1000); //动画显示ID="doTop"的这个层
			// $("#button1").show(),1000);  //动画显示ID="doTop"的这个层
		}
	});

	// /* 点击返回顶部箭头的事件 */
	// $("#btntop").click(function(){
	//         $("body,html").animate({scrollTop:0},500); //返回顶部，用JQ的animate动画
	// });


	/* 点击返回底部箭头的事件 */
	$("#button1").click(function() {
		$("body,html").animate({
			scrollTop: document.body.clientHeight
		}, 700); //返回底部，用JQ的animate动画
	});


});


