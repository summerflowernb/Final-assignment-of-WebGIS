//通过JavaScript的prototype属性继承于BMap.Control
ZoomControl.prototype = new BMap.Control();

//自定义控件必须实现自己的initialize方法，并且将控件的DOM元素返回
//在本方法中创建个div元素作为控件的容器，并将其添加到地图容器中
ZoomControl.prototype.initialize = function(map) {
	//创建一个dom元素
	var div = document.createElement('div');
	//添加文字说明
	div.appendChild(document.createTextNode(messageString1 + " " + messageString2));
	// 设置样式
	div.style.cursor = "pointer";
	div.style.padding = "7px 10px";
	div.style.boxShadow = "0 2px 6px 0 rgba(27, 142, 236, 0.5)";
	div.style.borderRadius = "5px";
	div.style.backgroundColor = "white";
	// 添加DOM元素到地图中
	map.getContainer().appendChild(div);
	// 将DOM元素返回
	return div;
}


//搜索函数2（关注房子的所在区属和价格)
function search2(b, c, d) {
	map.clearOverlays();
	for (var i = 0; i < house.length; i++) {
		if (b == house[i][0] || b == -1) {

			//此时已经满足社区区属的要求
			var pricestr = price[i][1].trim(); //清除字符串的空格
			for (var j = 0; j < pricestr.length; j++) { //将price字符串中的数字提取出来
				if (pricestr[j] >= '0' && pricestr[j] <= '9') {
					continue;
				} else {
					pricestr = pricestr.substring(0, j);
					break;
				}
			}
			var pricee = parseInt(pricestr); //得到当前房屋的价格
			if (pricee >= c && pricee <= d) {
				//此时房屋已经满足所有要求
				//将其加进地图
				var marker = new BMap.Marker(new BMap.Point(house[i][1], house[i][2])); // 创建标注
				var content = "小区：" + price[i][0] + "</br></br>" + "价格：" + price[i][1];
				map.addOverlay(marker); // 将标注添加到地图中
				addClickHandler(content, marker);
			}


		}
	}
}


// 点击社区标记的函数
function addClickHandler(content, marker) {
	marker.addEventListener("click", function(e) {
		map.removeOverlay(circle);
		drawCircle(e);
		openInfo(content, e)
	});

}

function drawCircle(e) {

	var p = e.target;
	mPoint = new BMap.Point(p.getPosition().lng, p.getPosition().lat);
	circle = new BMap.Circle(mPoint, 1500, {
		fillColor: "blue",
		strokeWeight: 1,
		fillOpacity: 0.1,
		strokeOpacity: 0.1
	});

	map.addOverlay(circle);
	map.centerAndZoom(mPoint, 14);
}

function openInfo(content, e) {
	var p = e.target;
	var point = new BMap.Point(p.getPosition().lng, p.getPosition().lat);
	var infoWindow = new BMap.InfoWindow(content, opts); // 创建信息窗口对象 
	map.openInfoWindow(infoWindow, point); //开启信息窗口
}

